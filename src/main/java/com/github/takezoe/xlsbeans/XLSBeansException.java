package com.github.takezoe.xlsbeans;

public class XLSBeansException extends Exception {

  private static final long serialVersionUID = 347909954203050508L;

  public XLSBeansException() {
    super();
  }

  public XLSBeansException(String message, Throwable cause) {
    super(message, cause);
  }

  public XLSBeansException(String message) {
    super(message);
  }

  public XLSBeansException(Throwable cause) {
    super(cause);
  }

}
