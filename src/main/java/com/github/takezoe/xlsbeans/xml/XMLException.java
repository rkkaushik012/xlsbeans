package com.github.takezoe.xlsbeans.xml;

import com.github.takezoe.xlsbeans.XLSBeansException;

public class XMLException extends XLSBeansException {

  private static final long serialVersionUID = 4762483379595821097L;

  public XMLException() {
    super();
  }

  public XMLException(String message, Throwable cause) {
    super(message, cause);
  }

  public XMLException(String message) {
    super(message);
  }

  public XMLException(Throwable cause) {
    super(cause);
  }

}
